package com.werkflow.entity;

/**
 * CapEx Request Category Enumeration
 * Categorizes different types of capital expenditures
 */
public enum CapExCategory {
    /**
     * Infrastructure related CapEx (buildings, facilities)
     */
    INFRASTRUCTURE,

    /**
     * Information Technology and systems
     */
    INFORMATION_TECHNOLOGY,

    /**
     * Machinery and equipment purchases
     */
    MACHINERY_EQUIPMENT,

    /**
     * Vehicles and transportation
     */
    VEHICLES,

    /**
     * Furniture and office fixtures
     */
    FURNITURE_FIXTURES,

    /**
     * Software licenses and implementations
     */
    SOFTWARE_LICENSES,

    /**
     * Maintenance and renovations
     */
    MAINTENANCE_RENOVATION,

    /**
     * Research and development equipment
     */
    RESEARCH_DEVELOPMENT,

    /**
     * Other capital expenditures
     */
    OTHER
}
